/*在美国的体积测量系统中，一品脱等于2杯，一杯等于8盎司，
1盎司等于2大汤勺，一大汤勺等于3茶勺.编写一个程序，
提示用户输入杯数，并以品脱，盎司，汤勺，茶勺为单位显示等价容量
思考对于该程序，为何使用浮点类型比整型类型更合适？
*/
#include <stdio.h>
int main(void)
{
	float a, b, c, d, e;
	b = 2.0f;		//品脱
	c = 8.0f;		//盎司
	d = 2.0f;		//大汤勺
	e = 3.0f;		//茶勺

	printf("请输入杯数:");
	scanf("%f", &a);
	printf("换算为品脱为%.2f,盎司为:%.2f,汤勺为:%.2f,茶勺为:%.2f", a / b,a*b,a*c*d,a*c*d*e);
	getchar();
	getchar();
	return 0;


}